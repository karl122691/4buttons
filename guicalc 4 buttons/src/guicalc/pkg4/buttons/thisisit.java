    private void divideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_divideActionPerformed
    try {
        number1 = Double.parseDouble(num1.getText());
        number2 = Double.parseDouble(num2.getText());
        if (number2 == 0) {
            JOptionPane.showMessageDialog(null, "Cannot divide by zero", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            result = number1 / number2;
            Ans.setText(String.valueOf(result));
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Please enter valid numbers", "Invalid Input", JOptionPane.ERROR_MESSAGE);
    }       // TODO add your handling code here:
    }//GEN-LAST:event_divideActionPerformed

