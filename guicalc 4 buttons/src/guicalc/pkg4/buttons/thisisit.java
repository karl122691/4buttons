    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
    try {
        number1 = Double.parseDouble(num1.getText());
        number2 = Double.parseDouble(num2.getText());
        result = number1 + number2;
        Ans.setText(String.valueOf(result));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Please enter valid numbers", "Invalid Input", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_addActionPerformed

